      <h1>Human nature?</h1>
      <div class="paragraph">
        As Ralph Waldo Emerson said, “To be yourself in a world that is constantly trying to make you someone else is the greatest accomplishment”. However, to what extent can we retain a realistic characteristic if most things we learn root from the collective knowledge of society? If humans are innately evil, according to Thomas Hobbes, shouldn't we strive for a better self by adopting the admirable characteristics of others? And by doing so, aren't we all fake by not preserving our own personalities?
      </div>
      <div class="paragraph">
        Thankfully, Hobbes' philosophy is not widely believed by North American culture. In my Theory of Knowledge class, everyone with the exception of two three believed that humans are born with a mix of some innocent traits, and some evil traits; and the other two people believed that humans are innately innocent.
      </div>
      <div class="paragraph">
        What about my belief? Well, my ideas are often more divergent. After a spicy encounter with a girl who I deify, I believe that most people are born innocent, but I was born fake.
      </div>
      <h1>The deity</h1>
      <div class="paragraph">
        Language is equivocal. Unlike science and math, rules in language arts are inconsistent. Writings can be opened to a duplicity of interpretations, and the same message can also be conveyed through different methods with creativity. I am more of a logos person than I am pathos and ethos. I often feel unmitigated to be in my English IB class where all of my classmates are enlightened with transcendental writing and interpretation skills. Thus, I respect those who are adequate in language arts for their gifted skills that I failed acquired despite the effort I put in.
      </div>
      <div class="paragraph">
        Of these English excelsiors, I especially admire a girl from my English class, who not only uses words like “transcendental”, “anaphora”, and “bravado” casually to share her creative yet logical intelligence in class, but also expertizes drama and improv. Oh, and if that is not enough to impress you, she published a book in grade 11.
      </div>
      <div class="paragraph">
        My feeling for her isn't romantic love or lust. It is purely reverence. She is a role model who I look up to and try to learn from. A role model who reminds me to be humble and thus motivates me to improve. A role model who makes me discard my characteristics to adopt more of hers.
      </div>
      <h1>The finale night</h1>
      <div class="paragraph">
        With such admiration, during the week of our school's One Act Festival, I of course did not let the opportunity to savour her plays slip through. I went to all the days with plays either directed by or staring her, with <a href="https://voovs.github.io" target="_blank" rel="noopener noreferrer">my friend Vova</a>, who is also a worshipper of her. On the finale night, I took down the plays' posters around the school that were no longer in use, and planned to ask for autographs from actors of plays I enjoyed.
      </div>
      <div class="paragraph">
        As the night continued, it came to the time for the goddess' play: <em>Hope 'n Mercy</em> — the most insightful and emotional play throughout the festival. Undoubtedly the hardest to act. Expectedly, she perfectly killed that play with her undeniably phenomenal acting. The cathartic anger, sadness, and the unwavering accent that retained even when she was shouting; the multiplicity of characters that she played by changing clothes quickly offstage; and the transition smoother than a puck sliding on ice, altogether gave me a completely fresh experience of enjoying plays.
      </div>
